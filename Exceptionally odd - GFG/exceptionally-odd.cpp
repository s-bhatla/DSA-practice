class Solution{   
public:
    int getOddOccurrence(int arr[], int n) {
        // code here
        //when we XOR all the numbers, the ones which will occur an even number of times will get cancelled out
        //As n^n = 0, and 0^n = n
        int ans = 0;
        for(int i = 0; i < n; i++){
            ans = ans^arr[i];
        }
        return ans;
    }
};

